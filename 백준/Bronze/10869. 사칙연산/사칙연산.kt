package com.example.basicsyntax

import java.util.Scanner

fun main() {
   val sc = Scanner(System.`in`)
    val value1 = sc.nextInt()
    val value2 = sc.nextInt()

    println(value1+value2)
    println(value1-value2)
    println(value1*value2)
    println(value1/value2)
    println(value1%value2)


}